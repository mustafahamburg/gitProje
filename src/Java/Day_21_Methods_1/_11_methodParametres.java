package com.Java.Day_21_Methods_1;

public class _11_methodParametres {
    public static void main(String[] args) {

        toplama(15,36,9); //60
    }

    public static void toplama(int x, int y, int z){
        //Burada sabit sekilde olan iki sayinin toplamini yazdik
        int sayi1=15;
        int sayi2=10;
        System.out.println(sayi1+sayi2);

        //Burada parametrelerin toplamini yazdiran statement veya body code yazdik
        System.out.println(x+y+z);
    }
}
