package Java.Day_40_Constructor_2.Example_2;

public class SDET {

    String name;
    int age;
    char gender;   // cinsiyet
    String company;
    double salary;

    public SDET(String name, int age, char gender, String company, double salary) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.company = company;
        this.salary = salary;
    }
}
