package com.Java.Day_17_ForLoops;

public class _2_ForLoopEvenNumber {
    public static void main(String[] args) {

        //Cift olan sayilarin yazdirilmasi 1 den 100 e kadar

        for (int i=1; i<=100; i++){  // kod blogunun icerisinde if condition var

            if (i%2==0){ // dongudeki sayinin cift olma durumunu kontrol ediyoruz.
                System.out.print(i+" "); // Burada ln nin olmamas durumu ve arada bosluk olusturup yazdirma sekli
            }

        }



    }
}
