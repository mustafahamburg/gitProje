package Java.Day_30_DateAndTime;

public class _4_stringLength {
    public static void main(String[] args) {
        // length()  String in uznlgunu verir yani kac karakter..
        String str="Java";
        String tex="euroTechStudy";
        int x1=str.length();
        System.out.println("x1 = " + x1);  // 4
        System.out.println("tex.length() = " + tex.length());  // 13
        System.out.println("tex.length()*10 = " + tex.length() * 10);  // 130

    }
}
